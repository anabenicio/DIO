package Exercicio01;

public class Operacoes {

	public float Soma(float num1, float num2) {
		
		return num1+num2;
		
	}
	public float Multiplicacao(float num1, float num2) {
		
		return num1*num2;
		
	}
	public float Divisao(float num1, float num2) {
		
		return num1/num2;
		
	}
	public float subtracao(float num1, float num2) {
		
		return num1-num2;
		
	}
}
